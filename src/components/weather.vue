<script setup>
import { ref } from 'vue';

               const api_key = 'f114c1d8edfe049f0d1de15551609120';
               const url_base = 'https://api.openweathermap.org/data/2.5/';
               let query = ref(''); 
               let weather = ref({});
 
    
        
             async function fetchWeather() {

                    await fetch(`${url_base}weather?q=${query.value}&units=metric&APPID=${api_key}`)
                    .then(res => {
                        return res.json();
                    })
                    .then(setResults);
                
            };

            function setResults(results) {
                weather.value = results;
                //console.log(results);
            };
    
            function dateBuilder() {
                let d = new Date();
    
                let months = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];
                let days = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];
    
                let day = days[d.getDay()];
                let date = d.getDate();
                let month = months[d.getMonth()];
                let year = d.getFullYear();
                return `${day} ${date} ${month} ${year}`;
            }
    
            
        
    

    </script>

<template>
    <div>
        
        <div class="search-box">
            <input 
            type="text" 
            class="search-bar" 
            placeholder="search..."
            v-model="query"
            @keypress.enter.prevent="fetchWeather"
            >
        </div>
        

        <div class="weather-wrap" v-if="typeof weather.main != 'undefined'">
            <div class="location-box">
                <div class="location">{{ weather.name }}</div>
                <div class="date">{{ dateBuilder() }}</div>
            </div>
        

            <div class="weather-box">
                <div class="temp">{{ Math.round(weather.main.temp) }}Â°</div>
                <div class="weather"> {{ weather.weather[0].description }}</div>
                <div class="wind">

                     {{ weather.wind.speed }}  
                     <span v-if="weather.wind.speed > 5" class="material-symbols-outlined shake"> air </span>
                     <span v-if="weather.wind.speed < 5" class="material-symbols-outlined shake"> airware </span>
                </div>
            </div>

        </div>
        <div class="weather-wrap" v-else>
                <div class="location-box">
                <div class="location">City not found</div>
                <div class="date">{{ dateBuilder() }}</div>
            </div>
        </div>
    </div>
</template>

